# SECTION 1: HEADER
# ══════════════════════════════════════════════════════════════════════════════
#
# Estimates key migration dates for Atlantic salmon post-smolts from two river
# systems (Saint Jean and Trinite) using acoustic telemetry detection data.
# Computes least-cost swimming distances between receiver stations and derives
# a mean swimming speed, which is then used to define migration windows across
# the Strait of Belle Isle, southern Labrador, and the Labrador Sea.
#
# Inputs:
#   - R/00_raw_data/telemetry/master_dataset_smolt_manuscript_dec_10_2025 QC.csv
#       Acoustic telemetry detection data (CSV, semicolon-separated, comma decimal)
#       Confidential — see DATA IMPORT section for access instructions.
#   - R/00_derived_data/Year_Median_Date_StJean.fst
#       Median smolting date by year for Saint Jean River (FST format)
#       Generated by running: 01_SmoltingDates_StJean.R
#   - R/00_derived_data/Year_Median_Date_Trinite.fst
#       Median smolting date by year for Trinite River (FST format)
#       Generated by running: 01_SmoltingDates_Trinite.R
#
# Outputs:
#   - R/00_derived_data/Year_Migration_Dates_River_Sea.fst
#       Migration date windows by river and cohort year (FST format)
#   - 02_PrepareData_Scripts/SST/MigrationDates_Distributions_FINAL.pdf
#       PDF figure showing receiver station map and migration date distributions
# ══════════════════════════════════════════════════════════════════════════════




