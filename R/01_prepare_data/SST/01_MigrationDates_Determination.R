# ── Compute least-cost distances between receiver stations ────────────────
#
# Least-cost path distances are computed through water only (land = NA),
# which prevents straight-line paths from cutting across land masses.
# A 0.001-degree resolution raster is used to balance accuracy and
# computation time. The transition surface is corrected for geographic
# distance using geoCorrection() so that costDistance() returns meters.

# Extract unique station coordinates within the target region
station_distances <- tel %>%
  dplyr::select(SerialNo_Rec, Station_Name, Latitude, Longitude, hit_location)

stations_sf <- station_distances %>%
  dplyr::filter(
    Latitude >= 50.5,
    hit_location %in% c(
      "gulf_of_st_lawrence_1", "strait_of_belle_isle",
      "newfoundland_1", "southern_labrador", "st_anthony"
    )
  ) %>%
  distinct(SerialNo_Rec, Latitude, Longitude, hit_location) %>%
  st_as_sf(coords = c("Longitude", "Latitude"), crs = 4326)

# Load country polygons for land masking
land <- ne_countries(scale = "large", returnclass = "sf")

ggplot() +
  geom_sf(data = land) +
  geom_sf(data = stations_sf, aes(color = hit_location)) +
  coord_sf(
    xlim   = c(bbox["xmin"] - 10, bbox["xmax"] + 2),
    ylim   = c(bbox["ymin"] - 5,  bbox["ymax"]),
    expand = FALSE
  )

# Build a cost raster: water = 1, land = NA (impassable)
ext    <- ext(stations_sf) + 0.1  # Extend to include all station areas
r      <- rast(extent = ext, resolution = 0.001, crs = "EPSG:4326")
values(r) <- 1                    # Initialize all cells as water
r_land <- rasterize(vect(land), r, field = 1)
r[r_land == 1] <- NA              # Land cells are impassable

plot(r)
plot(st_geometry(stations_sf), add = TRUE, pch = 16, col = "red", cex = 0.7)

# Reproject to UTM zone 20N (meters) for distance calculations
stations_sf <- st_transform(stations_sf, 32620)
land        <- st_transform(land, 32620)

# Convert to RasterLayer (required by gdistance::transition())
r_raster <- raster(r)
r_raster <- projectRaster(r_raster, crs = 32620, method = "bilinear")

# Build the transition surface (8-direction movement) and apply geographic correction
tr <- transition(r_raster, function(x) 1 / mean(x), 8)
tr <- geoCorrection(tr, type = "c")

# Compute the full pairwise least-cost distance matrix (in meters)
dist_mat_m <- costDistance(
  tr,
  st_coordinates(stations_sf),
  st_coordinates(stations_sf)
)

# Reshape distance matrix to long format for joining to individual data
ids               <- stations_sf$SerialNo_Rec
rownames(dist_mat_m) <- ids
colnames(dist_mat_m) <- ids

dist_long <- reshape2::melt(
  dist_mat_m,
  varnames   = c("SerialNo_Rec_entry", "SerialNo_Rec_exit"),
  value.name = "distance_m"
)


# ── Compute travel time and swimming speed per individual ─────────────────

# Join distances to paired entry-exit records
distances_id <- merge(
  tel_distances_sobi, dist_long,
  by = c("SerialNo_Rec_entry", "SerialNo_Rec_exit")
) %>%
  dplyr::mutate(distance_km = distance_m / 1000) %>%
  dplyr::select(-distance_m)

distances_id <- distances_id %>%
  dplyr::select(
    id, origin_river, SerialNo_Rec_entry, SerialNo_Rec_exit,
    hit_location_entry, hit_location_exit, date_hit_entry,
    date_hit_exit, distance_km, fl_mm.y
  ) %>%
  dplyr::mutate(
    date_hit_entry = yday(date_hit_entry),
    date_hit_exit  = yday(date_hit_exit),
    travel_time    = date_hit_exit - date_hit_entry
  ) %>%
  # When entry is detected at both arrays, give priority to SOBI over the Gulf array
  group_by(id, SerialNo_Rec_exit) %>%
  slice_min(
    order_by = if_else(hit_location_entry == "strait_of_belle_isle", 0, 1),
    n = 1
  ) %>%
  ungroup() %>%
  # Remove cases where exit was detected before or on the same day as entry
  filter(travel_time > 0) %>%
  dplyr::mutate(
    swim_speed = distance_km / travel_time,
    region     = ifelse(
      origin_river %in% c("Restigouche River", "Cascapedia River", "Saint Jean River"),
      "Gaspesie", "North Shore"
    )
  )

# Mean swimming speed used to estimate transit time across the Labrador Sea polygon
mean_swim_speed <- round(mean(distances_id$swim_speed))  # km / day
