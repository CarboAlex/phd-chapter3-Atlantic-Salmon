# ── Post-smolt swimming speed from telemetry and literature ────────────────
#
# Literature estimates of Atlantic salmon post-smolt swimming speeds generally
# fall within approximately 14 to 22 km/d. Studies conducted in the Gulf of
# St. Lawrence during the early marine phase, shortly after river exit, report
# mean speeds of ~14-18 km/d (Lefevre et al. 2012) and ~17-22 km/d (Chaput
# et al. 2019). Similar values have been reported elsewhere in the North
# Atlantic, including the Faroe-Shetland Channel, where post-smolts covered
# 713-874 km in 38-51 days (14-21 km/d; Shelton et al. 1997). Observations
# from more offshore or later migration stages indicate comparable or slightly
# higher values, with mean speeds around 22 km/d reported in the Northeast
# Atlantic (Holst et al. 1996) and in the Labrador Sea (Reddin & Short 1991).
#
# It is important to note that many of these estimates are derived from fish
# in the first phase of their marine migration, often shortly after river exit,
# when individuals are smaller and may exhibit lower sustained swimming speeds.
# In contrast, our estimate of a mean swimming speed of approximately 23 km/d
# is based on fish originating from the same river systems and observed farther
# along their migration, in the Strait of Belle Isle region. At this stage,
# individuals are expected to be slightly larger and more developmentally
# advanced, which is expected to translate into higher swimming capacity.
# Consequently, although our estimate lies at the upper end of the range
# reported in the literature, it remains biologically plausible and consistent
# with existing studies when differences in fish size and migration stage are
# taken into account.
#
# Thus, considering that the polygon at the entrance to the Labrador Sea is
# 300 km wide, we estimate that it will take (300 km) / (23 km/day) = 13 days
# to cross this polygon.
#
# | Source               | Distance / Time              | Average speed     |
# | -------------------- | ---------------------------- | ----------------- |
# | Lefevre et al. 2012  | ~630-800 km in 44 d          | ~14.4-18.2 km/d   |
# | Chaput et al. 2019   | ~800 km in ~36-48 d          | ~17-22 km/d       |
# | Shelton et al. 1997  | 713-874 km in 38-51 d        | 14-21 km/d        |
# | Holst et al. 1996    | ~2000 km                     | ~22 km/d          |
# | Reddin & Short, 1991 |                              | ~22 km/d          |
#
# References:
# Lefevre, M. A., Stokesbury, M. J., Whoriskey, F. G., & Dadswell, M. J.
#   (2012). Atlantic salmon post-smolt migration routes in the Gulf of
#   St. Lawrence. ICES Journal of Marine Science, 69(6), 981-990.
# Chaput, G., Carr, J., Daniels, J., Tinker, S., Jonsen, I., & Whoriskey, F.
#   (2019). Atlantic salmon (Salmo salar) smolt and early post-smolt migration
#   and survival inferred from multi-year and multi-stock acoustic telemetry
#   studies in the Gulf of St. Lawrence, northwest Atlantic. ICES Journal of
#   Marine Science, 76(4), 1107-1121.
# Shelton, R. G. J., Turrell, W. R., Macdonald, A., McLaren, I. S., & Nicoll,
#   N. T. (1997). Records of post-smolt Atlantic salmon, Salmo salar L., in
#   the Faroe-Shetland Channel in June 1996. Fisheries Research,
#   31(1-2), 159-162.
# Holst, J. C., Hansen, L. P., & Holm, M. (1996). Observations of abundance,
#   stock composition, body size and food of postsmolts of Atlantic salmon in
#   the NE Atlantic during summer.
# Reddin, D. G., & Short, P. B. (1991). Postsmolt Atlantic salmon (Salmo
#   salar) in the Labrador Sea. Canadian Journal of Fisheries and Aquatic
#   Sciences, 48(1), 2-6.


