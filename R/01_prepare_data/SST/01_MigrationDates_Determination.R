# ── Functions ──────────────────────────────────────────────────────────────

# get_path
# Purpose: Computes the shortest water path between two receiver stations
#          using the pre-built least-cost transition surface.
# Parameters:
#   entry   - SerialNo_Rec identifier for the entry station
#   exit    - SerialNo_Rec identifier for the exit station
#   fish_id - Individual fish identifier (id column)
#   river   - River of origin for the fish
# Returns: An sf LINESTRING object with the shortest path, or NULL if
#          either station is missing from stations_proj.
get_path <- function(entry, exit, fish_id, river) {
  
  start_pt <- stations_proj %>% filter(SerialNo_Rec == entry)
  end_pt   <- stations_proj %>% filter(SerialNo_Rec == exit)
  
  if (nrow(start_pt) == 0 | nrow(end_pt) == 0) return(NULL)
  
  path_sp <- shortestPath(
    tr,
    st_coordinates(start_pt),
    st_coordinates(end_pt),
    output = "SpatialLines"
  )
  
  proj4string(path_sp) <- CRS("+proj=utm +zone=20 +datum=WGS84 +units=m +no_defs")
  
  path_sf       <- st_as_sf(path_sp)
  path_sf$id    <- fish_id
  path_sf$river <- river
  path_sf$entry <- entry
  path_sf$exit  <- exit
  
  return(path_sf)
}

# Compute all individual migration paths
paths_list <- pmap(
  list(
    pairs$SerialNo_Rec_entry,
    pairs$SerialNo_Rec_exit,
    pairs$id,
    pairs$origin_river
  ),
  get_path
)

