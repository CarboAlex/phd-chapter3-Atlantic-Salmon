# ── plot_cor ───────────────────────────────────────────────────────────────────
# Purpose: Produces a scatterplot with a linear smoother and Pearson correlation
#          annotation for two variables in a data frame.
# Parameters:
#   data  : data frame containing the variables to plot
#   var_x : character string, name of the x-axis variable
#   var_y : character string, name of the y-axis variable
#   title : optional character string for the plot title
# Returns: a ggplot object

plot_cor <- function(data, var_x, var_y, title = NULL) {
  
  r_val  <- cor(data[[var_x]], data[[var_y]])
  p_val  <- cor.test(data[[var_x]], data[[var_y]])$p.value
  
  p_label <- if (p_val < 0.001) "< 0.001" else as.character(round(p_val, 3))
  
  ggplot(data, aes(x = .data[[var_x]], y = .data[[var_y]])) +
    geom_point(color = "#2c7bb6", size = 2.5) +
    geom_smooth(method = "lm", color = "#d73027", fill = "#d73027",
                alpha = 0.1, linewidth = 1) +
    annotate("text",
             x = -Inf, y = Inf,
             hjust = -0.15, vjust = 1.5,
             label = paste0("r = ", round(r_val, 2), "\np = ", p_label),
             size = 4, fontface = "italic", color = "grey30") +
    labs(title = title, x = var_x, y = var_y) +
    theme_minimal(base_size = 13) +
    theme(plot.title = element_text(face = "bold", size = 11))
}




