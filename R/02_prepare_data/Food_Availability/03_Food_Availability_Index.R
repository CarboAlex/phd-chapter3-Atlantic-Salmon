pca_tri_2sw_plk$scores[, 1] <- pc1_tri_2sw_plk


# ── TRI — Assemble food availability index ────────────────────────────────────

# Combine 1SW and 2SW PC1 scores into a single data frame indexed by cohort.
food_index_tri <- data.frame(
  cohort       = plankton_tri_1sw$cohort[as.integer(rownames(pca_data_tri_1sw_plk))],
  river        = "tri",
  food_index_1 = pca_tri_1sw_plk$scores[, 1]
) %>%
  left_join(
    data.frame(
      cohort           = plankton_tri_1sw$cohort[
        as.integer(rownames(as.data.frame(pca_tri_1sw_plk$scores)))
