# ── TRI 1SW — PCA input preparation ───────────────────────────────────────────

# Select zooplankton biomass at t and t+1 for 1SW cohorts.
pca_data_tri_1sw_plk <- plankton_tri_1sw %>%
  dplyr::select(cohort, Plankton_t) %>%
  arrange(cohort) %>%
  mutate(Plankton_tp1 = lag(Plankton_t, n = 1)) %>%
  dplyr::select(-cohort) %>%
  na.omit()

cor_tri_1sw <- cor(pca_data_tri_1sw_plk, use = "complete.obs")

# Correlation check before running PCA.
p_cor_tri_1sw <- pca_data_tri_1sw_plk %>%
  ggplot(aes(x = Plankton_t, y = Plankton_tp1)) +
  geom_point(color = "#2c7bb6", size = 2.5) +
  geom_smooth(method = "lm", color = "#d73027", fill = "#d73027",
              alpha = 0.1, linewidth = 1) +
  annotate("text",
           x = -Inf, y = Inf,
           hjust = -0.15, vjust = 1.5,
           label = paste0("r = ", round(cor_tri_1sw[1, 2], 2),
                          "\np = ", round(cor.test(pca_data_tri_1sw_plk$Plankton_t,
                                                   pca_data_tri_1sw_plk$Plankton_tp1)$p.value, 3)),
           size = 4, fontface = "italic", color = "grey30") +
  labs(title = "TRI 1SW — Variable correlation",
       x = "Capelin biomass (t+1)", y = "Zooplankton biomass (t)") +
  theme_minimal(base_size = 13) +
  theme(plot.title = element_text(face = "bold"))
p_cor_tri_1sw


# ── TRI 1SW — PCA ─────────────────────────────────────────────────────────────

# cor = TRUE standardizes variables before computing the PCA.
pca_tri_1sw_plk <- princomp(pca_data_tri_1sw_plk, cor = TRUE)
summary(pca_tri_1sw_plk)
biplot(pca_tri_1sw_plk)

# PC1 sign is arbitrary; align it so higher values = higher biomass.
pc1_tri_1sw_plk      <- pca_tri_1sw_plk$scores[, 1]
biomass_mean_1sw_plk <- rowMeans(pca_data_tri_1sw_plk, na.rm = TRUE)
if (cor(pc1_tri_1sw_plk, biomass_mean_1sw_plk, use = "complete.obs") < 0) {
  pc1_tri_1sw_plk <- -pc1_tri_1sw_plk
}
pca_tri_1sw_plk$scores[, 1] <- pc1_tri_1sw_plk


# ── TRI 2SW — PCA input preparation ───────────────────────────────────────────

# Select zooplankton biomass at t, t+1, and t+2 for 2SW cohorts.
pca_data_tri_2sw_plk <- plankton_tri_2sw %>%
  dplyr::select(cohort, Plankton_t, Plankton_tp1) %>%
  arrange(cohort) %>%
  mutate(Plankton_tp2 = lag(Plankton_t, n = 2)) %>%
  dplyr::select(-cohort) %>%
  na.omit()

# Check pairwise correlations among all lag variables.
var_pairs <- combn(names(pca_data_tri_2sw_plk), 2, simplify = FALSE)

plots <- map(var_pairs, ~ plot_cor(
  data  = pca_data_tri_2sw_plk,
  var_x = .x[1],
  var_y = .x[2],
  title = paste0(.x[1], " ~ ", .x[2])
))

# Assemble correlation plots into a grid.
p_cor_tri_2sw_plk_all <- wrap_plots(plots, ncol = 3) +
  plot_annotation(
    title = "TRI 2SW — Zooplankton biomass lag correlations",
    theme = theme(plot.title = element_text(face = "bold", size = 14))
  )
p_cor_tri_2sw_plk_all


# ── TRI 2SW — PCA ─────────────────────────────────────────────────────────────

