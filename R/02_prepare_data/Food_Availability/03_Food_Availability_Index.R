# Builds a zooplankton food availability index (PC1 from PCA) for Atlantic
# salmon sea-age classes (1SW, 2SW) for the Trinite and Saint-Jean rivers.
# Plankton biomass values at lags t, t+1, and t+2 are used as PCA inputs;
# PC1 orientation is aligned so that higher values indicate higher biomass.
#
# Inputs:
#   - Plankton_LabradorSea_tri_1SW.fst  : annual plankton biomass, TRI 1SW
#   - Plankton_LabradorSea_tri_2SW.fst  : annual plankton biomass, TRI 2SW
#   - Plankton_LabradorSea_stj_1SW.fst  : annual plankton biomass, STJ 1SW
