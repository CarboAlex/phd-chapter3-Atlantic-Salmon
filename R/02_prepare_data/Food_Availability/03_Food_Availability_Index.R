#
# Outputs:
#   - Food_Index_tri_1SW.fst  : plankton index (PC1), TRI 1SW (.fst)
#   - Food_Index_tri_2SW.fst  : plankton index (PC1), TRI 2SW (.fst)
#   - Food_Index_stj_1SW.fst  : plankton index (PC1), STJ 1SW (.fst)
#   - Food_Index_stj_2SW.fst  : plankton index (PC1), STJ 2SW (.fst)
# ==============================================================================




