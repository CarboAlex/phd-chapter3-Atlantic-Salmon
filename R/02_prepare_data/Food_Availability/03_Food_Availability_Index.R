      plankton_index_1 = pca_tri_1sw_plk$scores[, 1]
    ), by = "cohort"
  ) %>%
  left_join(
    data.frame(
      cohort           = plankton_tri_2sw$cohort[
        as.integer(rownames(as.data.frame(pca_tri_2sw_plk$scores)))
      ],
      plankton_index_2 = pca_tri_2sw_plk$scores[, 1]
    ), by = "cohort"
  )


