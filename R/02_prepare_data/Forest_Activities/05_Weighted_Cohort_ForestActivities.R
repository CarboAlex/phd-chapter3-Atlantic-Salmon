# ── weighted_co ───────────────────────────────────────────────────────────────
# Purpose: Computes a cohort-weighted mean of an environmental variable for
#   each salmon cohort, weighting each year by the proportion of individuals
#   still in freshwater at that age.
# Parameters:
#   smolt_data  data frame with columns cohort, smolt_prop_2yr, smolt_prop_3yr,
#               smolt_prop_4yr, smolt_prop_5yr
#   env_data    data frame with columns year and area (annual env. variable)
# Returns: data frame with columns cohort and co_mean (weighted mean area),
#   restricted to cohorts with at least one non-NA age-class proportion.

weighted_co <- function(smolt_data, env_data) {
  
  years            <- unique(smolt_data$cohort)
  smolt_data$co_mean <- NA
  
  for (i in seq_along(years)) {
    
    smolt_data_i <- smolt_data %>%
      dplyr::filter(cohort == years[i])
    
    # Extract disturbance area for each freshwater year of the cohort.
    # A disturbance at time t is assumed to affect salmon at time t+1,
    # so year i maps to the first growth season (t_0).
    t_0 <- env_data %>%
      dplyr::filter(year %in% c(years[i]))     %>% dplyr::pull(area)
    t_1 <- env_data %>%
      dplyr::filter(year %in% c(years[i] + 1)) %>% dplyr::pull(area)
    t_2 <- env_data %>%
      dplyr::filter(year %in% c(years[i] + 2)) %>% dplyr::pull(area)
    t_3 <- env_data %>%
      dplyr::filter(year %in% c(years[i]))     %>% dplyr::pull(area)
    t_4 <- env_data %>%
      dplyr::filter(year %in% c(years[i] + 4)) %>% dplyr::pull(area)
    t_5 <- env_data %>%
      dplyr::filter(year %in% c(years[i] + 5)) %>% dplyr::pull(area)
    
    # Replace NA proportions with 0 only for the minority age classes
    # (2yr and 5yr); NA in 3yr or 4yr propagates to co_mean intentionally.
    p2 <- ifelse(is.na(smolt_data_i$smolt_prop_2yr), 0,
                 smolt_data_i$smolt_prop_2yr)
    p3 <- ifelse(is.na(smolt_data_i$smolt_prop_3yr), 0,
                 smolt_data_i$smolt_prop_3yr)
    p4 <- ifelse(is.na(smolt_data_i$smolt_prop_4yr), 0,
                 smolt_data_i$smolt_prop_4yr)
    p5 <- ifelse(is.na(smolt_data_i$smolt_prop_5yr), 0,
                 smolt_data_i$smolt_prop_5yr)
    
    # Weights decrease as cumulative smoltification removes individuals
    # from the freshwater population across ages.
    smolt_data$co_mean[i] <- weighted.mean(
      x = c(t_0, t_1, t_2, t_3, t_4, t_5),
      w = c(rep(1,                              length(t_0)),
            rep(1,                              length(t_1)),
            rep(max(c(0, 1 - p2)),              length(t_2)),
            rep(max(c(0, 1 - p2 - p3)),         length(t_3)),
            rep(max(c(0, 1 - p2 - p3 - p4)),    length(t_4)),
            rep(max(c(0, 1 - p2 - p3 - p4 - p5)), length(t_5))),
      na.rm = FALSE
    )
  }
  
  # Remove cohorts where all age-class proportions are NA, as replacing
  # all NAs with 0 would produce heavily biased weighted means.
  smolt_data <- smolt_data %>%
    dplyr::filter(!(is.na(smolt_data$smolt_prop_2yr) &
                      is.na(smolt_data$smolt_prop_3yr) &
                      is.na(smolt_data$smolt_prop_4yr) &
                      is.na(smolt_data$smolt_prop_5yr))) %>%
    dplyr::select(cohort, co_mean) %>%
    as.data.frame()
  
  return(smolt_data)
}


