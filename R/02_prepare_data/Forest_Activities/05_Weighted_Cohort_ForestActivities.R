# ── Split smolt proportions by river ─────────────────────────────────────────

prop_smolt_sub_sj <- prop_smolt %>%
  dplyr::filter(river == "stj")

prop_smolt_sub_tr <- prop_smolt %>%
  dplyr::filter(river == "tri")


# ── Compute cohort-weighted means ─────────────────────────────────────────────

# Since the exact timing of disturbances within the year is unknown,
# disturbances from year t are assumed to affect salmon starting at t+1.
weighted_co_sj <- weighted_co(
  smolt_data = prop_smolt_sub_sj, env_data = pertu_stj) %>%
  dplyr::rename(co_mean_forest = co_mean)

weighted_co_tr <- weighted_co(
  smolt_data = prop_smolt_sub_tr, env_data = pertu_tri) %>%
  dplyr::rename(co_mean_forest = co_mean)

ggarrange(
  ggplot(data = weighted_co_sj, aes(x = cohort, y = co_mean_forest)) +
    geom_line() +
    labs(title = "St. Jean"),
  ggplot(data = weighted_co_tr, aes(x = cohort, y = co_mean_forest)) +
    geom_line() +
    labs(title = "Trinite"),
  ncol = 1)


# ── Export ────────────────────────────────────────────────────────────────────

weighted_co_sj$river <- "stj"
weighted_co_tr$river <- "tri"

data_all <- bind_rows(weighted_co_sj, weighted_co_tr) %>%
  dplyr::select(river, cohort, everything())

write_fst(data_all,
          file.path(base_path, "R/03_clean_data/River/ForestActivity_Cohort.fst"))




