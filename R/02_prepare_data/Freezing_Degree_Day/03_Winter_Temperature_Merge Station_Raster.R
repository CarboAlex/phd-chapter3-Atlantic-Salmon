#       Daily mean temperature derived from ERA5 rasters (FST)
#
# Outputs:
#   - R/01_derived_data/Winter_Temperature/tr_air_temp_daily_mean_combined.fst
#   - R/01_derived_data/Winter_Temperature/sj_air_temp_daily_mean_combined.fst
