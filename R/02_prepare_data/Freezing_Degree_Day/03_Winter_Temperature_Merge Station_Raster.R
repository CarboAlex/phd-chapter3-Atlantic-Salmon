# SOURCE: Canadian climate stations via ClimateData.ca
# STATUS: Too large for GitHub — not included in this repository.
#         Generated by running: Air_Temperature_Stations.R
tr_temp_summary_station <- read_fst(
  file.path(in_path, "tr_air_temp_daily_mean_stations.fst"))
sj_temp_summary_station <- read_fst(
  file.path(in_path, "sj_air_temp_daily_mean_stations.fst"))

colnames(tr_temp_summary_station)[1] <- "date"
colnames(sj_temp_summary_station)[1] <- "date"


# ── Raster-based daily means ──────────────────────────────────────────────────
# SOURCE: ERA5 reanalysis via Copernicus Climate Data Store
# STATUS: Too large for GitHub — not included in this repository.
#         Generated by running: Air_Temperature_Raster.R
tr_temp_summary_raster <- read_fst(
  file.path(in_path, "tr_air_temp_daily_mean_rasters.fst"))
sj_temp_summary_raster <- read_fst(
  file.path(in_path, "sj_air_temp_daily_mean_rasters.fst"))


# ══════════════════════════════════════════════════════════════════════════════
# SECTION 5: CONFIGURATION
